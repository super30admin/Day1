class MyHashSet:
    def __init__(self):
        self.buckets = 1000
        self.bucketItems = 1000
        self.storage = [None] * (self.buckets)

    def bucketItem(self, key) -> int:
        return key % self.bucketItems

    def bucket(self, key) -> int:
        return key % self.buckets

    def add(self, key: int) -> None:
        print("ADD: " + str(key))
        bucket = self.bucket(key)
        bucketItem = self.bucketItem(key)
        if self.storage[bucket] == None:
            if bucket == 0:
                self.storage[bucket] = [False] * (self.bucketItems + 1)
            else:
                self.storage[bucket] = [False] * (self.bucketItems)
        self.storage[bucket][bucketItem] = True
        return

    def remove(self, key: int) -> None:
        bucket = self.bucket(key)
        bucketItem = self.bucketItem(key)
        if self.storage[bucket] == None:
            print("Remove: None")
            return
        self.storage[bucket][bucketItem] = False
        print("Removed: " + str(key))
        return

    def contains(self, key: int) -> bool:
        bucket = self.bucket(key)
        bucketItem = self.bucketItem(key)
        if self.storage[bucket] == None:
            print("Contains: None")
            return False
        print("Contains: " + str(key))
        return self.storage[bucket][bucketItem]


