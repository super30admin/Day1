// Time Complexity : O(1) for all operations
// Space Complexity : O(N)


class MinStack {
    Stack<Integer> stack;
    Stack<Integer> minStack;
    public MinStack() {
        stack = new Stack<>();
        minStack = new Stack<>();
    }
    
    public void push(int val) {
        stack.push(val);
        if (minStack.empty()) {
            minStack.push(val);
        } else {
            int minStackTopVal = minStack.peek();
            if (minStackTopVal < val){
                minStack.push(minStackTopVal);
            } else {
                minStack.push(val);
            }
        }
    }
    
    public void pop() {
        stack.pop();
        minStack.pop();
    }
    
    public int top() {
        return stack.peek();
    }
    
    public int getMin() {
        return minStack.peek();
        
    }
}

